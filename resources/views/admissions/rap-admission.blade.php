                                        <h3 class="rap-note-title">Process for International Candidates</h3>
                                        <p>
                                            Admissions of international students are centralized for all the constituent
                                            institutes of Symbiosis International Deemed University and are routed
                                            through the Symbiosis Centre for International Education (SCIE).
                                        </p>
                                        <p>
                                            SCIE earmarks 20% of the total intake of the respective program for
                                            international students which include, Foreign Nationals (FN), Person of
                                            Indian Origin (PIO) Card Holders, Overseas Citizens of India (OCI) Card
                                            Holders, and Non-Resident Indians (NRI).
                                        </p>
                                        <div class="rap_international_candi_mainbox">
                                            <div class="rap_international_candi_listing">
                                                <div class="rap_international_candi_heading">Foreign National (FN)</div>
                                                <div class="rap_international_candi_para">
                                                    <p class="m-0">A student is eligible to apply as a Foreign National
                                                        (FN) if he/she is a citizen of any country other than India and
                                                        holds a foreign passport.</p>
                                                </div>
                                            </div>
                                            <div class="rap_international_candi_listing">
                                                <div class="rap_international_candi_heading">Person of Indian Origin
                                                    (PIO)</div>
                                                <div class="rap_international_candi_para">
                                                    <p class="m-0">As per the Government of India notification dated 9th
                                                        January 2015, all the existing Persons of Indian Origin
                                                        cardholders registered as such shall be deemed to be Overseas
                                                        Citizens of India cardholders.</p>
                                                </div>
                                            </div>
                                            <div class="rap_international_candi_listing">
                                                <div class="rap_international_candi_heading">Overseas Citizen of India
                                                    (OCI)</div>
                                                <div class="rap_international_candi_para">
                                                    <p class="m-0">A student is eligible to apply as OCI if he/she is
                                                        granted with Overseas Citizenship of India and has an OCI card.
                                                    </p>
                                                </div>
                                            </div>
                                            <div class="rap_international_candi_listing">
                                                <div class="rap_international_candi_heading">Non-Resident Indian (NRI)
                                                </div>
                                                <div class="rap_international_candi_para">
                                                    <p class="m-0">A student is eligible to apply as a Non-Resident
                                                        Indian (NRI) if he/she has appeared for either 10th or 10+2
                                                        (equivalent to the Indian 10th or 12th) examination outside
                                                        India. It is mandatory for one of the two qualifying
                                                        examinations to be given outside India. Please refer <a
