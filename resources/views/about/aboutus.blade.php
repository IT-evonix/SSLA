                                </li>
                                <li>
                                    B.A/B.Sc. (Liberal Arts) Honours with Research degree includes: 1
                                    Major Specialisation + 1 Minor Specialisation + Cores + Electives +
                                    COP + Internship + Research Project. For the Honours with Research
                                    degree, 12 credits will be completed through Proposal Writing (4
                                    credits) + Research Project (8 credits).
                                </li>
                                <li>
                                    B.A./B.Sc. (Liberal Arts) Honours degree includes: 1 Major
                                    Specialisation + 1 Minor Specialisation + Cores + Electives + COP +
                                    Internship + Additional Courses and/or Internship. For the Honours
                                    degree, 12 credits will be completed through Proposal Writing (4
                                    credits) + 2 Additional Courses (4+4 credits) and/or Internship (4
                                    credits).
                                </li>
                                <li>
                                    Students with CGPA 7.5 and above at the end of Semester 6 will be
                                    awarded B.A./B.Sc. (Liberal Arts) Honours with Research degree.
                                </li>
                                <li>
                                    Students with CGPA below 7.5 at the end of Semester 6 will be
                                    awarded a B.A./B.Sc. (Liberal Arts) Honours degree.
                                </li>
                                <li>
                                    Students with CGPA below 7.5 will have the option to pursue a
                                    Research Project. (8 Credits). The degree awarded will be B.A. /
                                    B.Sc. (Liberal Arts) Honours.
                                </li>
                                <li>
                                    All the Specialisation courses can be opted as General Elective /
                                    Audit / Extra from the 2nd year onwards as long as the
                                    prerequisites, if any, are fulfilled.
                                </li>
                                <li>
                                    Students have the option of accumulating extra credits by opting for
                                    Extra courses beyond the minimum prescribed credits for the award of
                                    the degree (without impacting their degree). Grades for Extra course
                                    will contribute towards calculating GPA and CGPA. Students may
                                    pursue a maximum of 1 Extra course per semester (4 credits) from
                                    semester 3 onwards.
                                </li>
                                <li>
                                    Research Project: Proposal Writing, Research Project and Summer
                                    Internship can be pursued in the Major specialisation.
                                </li>
                                <li>
                                    Students can opt for Audit courses over and above the programme
                                    requirements. Audit courses will not contribute towards the
                                    calculations of GPA or CGPA but will reflect in the SIU Grade Sheet
                                    as ‘Pass’ grade if the student has cleared the course. Students may
                                    pursue a maximum of 1 Audit course per semester (4 credits) from
                                    semester 3 onwards.
                                </li>
                                <li>
                                    Students undergoing the Global Immersion Programme (GIP) are
                                    entitled to GIP credits (4 credits / 8 credits / 12 credits / the
                                    total number of credits for the semester) based on applicable
