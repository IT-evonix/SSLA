                            <div class="directors_notes_contentbox">
                                <h3 class="inner_subheading">Director’s Profile</h3>
                                <p>
                                    Dr. Shweta Sinha Deshpande has a Ph.D. in Archaeology and has contributed to
                                    archaeological and associated anthropological research through an interdisciplinary
                                    and multidisciplinary approach. She has been associated with Liberal Arts education
                                    for more than a decade and has an in-depth and a keen understanding of this model
                                    and the value addition this model offers to the world and India’s growing population
                                    dividend, the globalized industry and the service sector. Her current research focus
                                    is on Globalization and its influence on the socio-cultural space including
                                    religion, trends in migration, education and development. The thrust of her research
                                    is on interactions between individuals and cultures, both modern and ancient. Her
                                    work has led her to conclude that the key to understanding current social issues is
                                    to recognize the often-unrealized cultural baggage that people carry.
                                </p>
                                <p class="m-0">
                                    She has authored many research articles and books and was the founding President of
                                    the India Big History Association and a founding Member of the Society for South
                                    Asia Archaeology (SOSAA). She currently serves on the Editorial Board of Migration
                                    Letters an international journal on migration and is a member of the Executive
                                    Committee of the Nehru Science Centre, Mumbai under the Ministry of Culture,
                                    Government of India.
                                </p>
