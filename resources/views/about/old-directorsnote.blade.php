                            <div class="directors_notes_contentbox">
                                <h3 class="inner_subheading">From the Director's Desk</h3>
                                <p>
                                    The Twenty-first century is unprecedented and crucial for the globe and humanity. It
                                    is the fastest-changing that history has ever seen and offers innumerable
                                    opportunities like never before to those who are quick at adapting and learning the
                                    new ways of this world. It is also vulnerable to the challenges and threats of the
                                    social, political, and physical environment leading to an ‘existential risk’. This
                                    dynamic world needs educated citizens equipped with skillsets to drive into
                                    uncharted territories and help shape our future.
                                </p>
                                <p>
                                    The world we engage with every day is not compartmentalised. It flows from one to
                                    the other. Culture, media, and education influence and shape the worldviews and
                                    lenses with which we view ourselves and the surroundings. Since it is not always
                                    within our hands to shape the culture or media that we engage with, education
                                    becomes the means that will lead to a critical comprehensive understanding.
                                </p>
                                <p>
                                    Education, according to Nelson Mandela, ‘is the most powerful weapon which can be
                                    used to change the world’. Education steers curiosity, and together, they become the
                                    means that allow individuals and societies to truly engage, comprehend and make
                                    sense of the world that we live in.
                                </p>
                                <p>
                                    Today technology has created a borderless experience of learning with access to
                                    unparalleled data, people and answers to questions. What is therefore needed, is the
                                    skill to make sense of the information that is around them. The education needed
                                    therefore is not facts, figures and information but the capability of creative and
                                    critical thinking; communication and collaboration for problem solving at social,
                                    professional and personal levels.
                                </p>
                                <p class="m-0">
                                    SSLA’s four-year B.A/B.Sc. Liberal Arts Honours degree moves beyond the traditional
                                    degree to promote an inter-multi-and-trans-disciplinary holistic and well-rounded
                                    education. It brings together the deep knowledge of individual disciplines and
                                    transferable skills of creative problem solving; reasoning and critical thinking,
                                    intercultural collaboration and team work with communication for the 21st century
                                    dynamic world. It provides the much-needed agility, adaptability and ability to
                                    identify and connect the dots to see the whole and the parts as it exists in the
                                    real world offering an opportunity for those who are interested in positions of
                                    responsibility and decision making in their professional, personal and public lives.
                                </p>
