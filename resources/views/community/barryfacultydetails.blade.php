                    <h4 class="section-title">Bio</h4>
                </div>
                <p>
                    Barry Rodrigue is a Geographer, Archaeologist and Ethnographer. He serves as Professor of
                    Anthropology and Scholar-in-Residence at Symbiosis International University in Pune, Maharashtra,
                    India. He began his career as an Ethnographer and Field Biologist in Alaska, where he studied
                    natural / cultural landscapes among indigenous communities. A Fulbright Scholar, he worked as a
                    Research Geographer at Laval University and Associate Professor of Humanities at the University of
                    Southern Maine. His strategy of integrating local, regional and global inquiry into a unified
                    context made him an early advocate of Universal Studies. As a founder of the International Big
                    History Association (2010) and the Asian Big History Association (2014), he served as their
                    international coordinator. He is a strong proponent of engaged scholarship, where researchers
                    collaborate with grassroot communities around the world to resolve problems through their shared
                    ideas and networks.

                </p>
                <p>
                    <strong>Dr. Barry Rodrigue</strong> served as the <strong>Contributing Editor</strong> for
                    <em>“The General Law of Being, Article 3: The Ultimate Cause of Evolution”</em> by Chen Ye,
                    published in the <strong>Journal of Big History</strong>.
                </p>
                <p>
                    Engaged in anthropological field research in collaboration with SSLA post-graduate
                    <strong>Sudev Madhav</strong> and indigenous communities:
                <ul>
                    <li>
                        In the <strong>BR Hills of southern Karnataka</strong>, documenting megalithic and other
                        heritage sites in partnership with the <strong>Soliga indigenous tribal
                            community</strong>.
                    </li>
                    <li>
                        In <strong>eastern India</strong>, collaborating with <strong>Koya tribal doctor Vaidya
                            Venkatraj Nidigonda</strong> and his family to document
                        <strong>ethnomedicine</strong> and
                        cultural traditions, with a particular focus on health in the context of the
                        <strong>climate crisis</strong>.
                    </li>
                </ul>
                </p>
